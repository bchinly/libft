#include <string.h>
#include <unistd.h>

char	*ft_strlcat(char *s1, char *s2, size_t n)
{
	char *str;

	str = s1;
	while (*s1 != '\0' && n  > 1)
	{
		*str++ = *s1++;
		n--;
	}
	while (*s2 != '\0' && n > 1)
	{
		*str++ = *s2++;
		n--;
	}
	*str = '\0';
	return (s1);
}
