int		ft_strlen(char *str);

char	*ft_strcat(char *s1, char *s2)
{
	char *str;

	str = s1;
	while (*s1 != '\0')
		*str++ = *s1++;
	while (*s2 != '\0')
		*str++ = *s2++;
	*str = '\0';
	return (s1);
}
