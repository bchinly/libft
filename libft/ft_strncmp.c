#include "libft.h"

int		ft_strncmp(unsigned char const *s1, unsigned char const *s2, size_t n)
{
	while ((*s1 == *s2)
			&& (*s1 != '\0' || *s2 != '\0')
			&& (n > 0))
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
