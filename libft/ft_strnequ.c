#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2)
	{
		if (ft_strncmp((unsigned char *)s1, (unsigned char *)s2, n) == 0)
			return (1);
	}
	return (0);
}
