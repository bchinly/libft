#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	int i;

	i = 0;
	if (s[i] == '\0')
	{
		if (c != '\0')
			return (NULL);
		return ((char *)&s[i]);
	}
	while (s[i] != '\0' && s[i] != c)
	{
		i++;
		if (s[i] == '\0' && c != '\0')
			return (NULL);
	}
	return ((char *)&s[i]);
}
